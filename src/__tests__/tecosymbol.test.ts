import { ShxFont } from '../font';
import { ShxFontData, ShxFontType } from '../fontData';

function createShapesFontData(shapes: Record<number, Uint8Array>): ShxFontData {
  return {
    header: {
      fontType: ShxFontType.SHAPES,
      fileHeader: 'AutoCAD-86 shapes 1.0',
      fileVersion: '1.0',
    },
    content: {
      data: shapes,
      info: 'test',
      orientation: 'horizontal',
      baseUp: 8,
      baseDown: 2,
      height: 10,
      width: 10,
      isExtended: false,
    },
  };
}

// Bytecode extracted from tecosymbol.shx gate-valve symbols (_GV_ / _GCD_).
// Shapes end with pen down and rely on end-of-shape (0x00) flush.
const GV_SHAPE = new Uint8Array([
  0x04, 0x50, 0x04, 0x7d, 0x03, 0x64, 0x03, 0x7f, 0x02, 0x08, 0x00, 0x00, 0x01, 0x08, 0xc1, 0x26,
  0x02, 0x08, 0x7f, 0x00, 0x01, 0x08, 0x00, 0xb4, 0x02, 0x08, 0xc0, 0x26, 0x01, 0x08, 0x40, 0x26,
  0x02, 0x08, 0x81, 0x00, 0x01, 0x08, 0x00, 0xb4, 0x02, 0x08, 0x7f, 0x00, 0x01, 0x08, 0xc0, 0x26,
  0x02, 0x08, 0xc1, 0xda, 0x01, 0x08, 0x3f, 0x26, 0x00,
]);

const GCD_SHAPE = new Uint8Array([
  0x04, 0x50, 0x04, 0x7d, 0x03, 0x64, 0x03, 0x7f, 0x02, 0x08, 0x00, 0xd9, 0x01, 0x08, 0x27, 0xe7,
  0x02, 0x08, 0x00, 0x7f, 0x01, 0x08, 0x00, 0x81, 0x02, 0x08, 0xb2, 0x19, 0x01, 0x08, 0x27, 0x00,
  0x02, 0x08, 0x00, 0x4e, 0x01, 0x08, 0x00, 0xb2, 0x02, 0x08, 0xd9, 0x4e, 0x01, 0x08, 0x00, 0xb2,
  0x02, 0x08, 0x27, 0x4e, 0x01, 0x08, 0x27, 0x18, 0x02, 0x08, 0xd9, 0xe8, 0x01, 0x08, 0xd9, 0x00,
  0x00,
]);

function polylineCount(font: ShxFont, code: number): number {
  return font.getCharShape(code, 10)?.polylines.length ?? 0;
}

describe('gate valve shapes ending with pen down', () => {
  const font = new ShxFont(
    createShapesFontData({
      9: GV_SHAPE,
      10: GCD_SHAPE,
    })
  );

  afterAll(() => {
    font.release();
  });

  it('_GV_ (code 9) includes the closing diagonal stroke', () => {
    expect(polylineCount(font, 9)).toBe(6);
  });

  it('_GCD_ (code 10) includes the closing horizontal stroke', () => {
    expect(polylineCount(font, 10)).toBe(7);
  });
});
